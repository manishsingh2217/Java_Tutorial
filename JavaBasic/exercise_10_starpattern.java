package JavaBasic;
public class exercise_10_starpattern
{
    public static void main(String[] args)
    {
            int i,j;
            for (i=0; i<3; i++)
            {
                for (j=0; j<6; j++)
                {
                    System.out.print("*");
                }
                System.out.println();
            }
    }
}
